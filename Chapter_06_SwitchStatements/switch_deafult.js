let day = 9;
switch(day){
    case 1:
        console.log("sunday");
        break;
    case 2:
        console.log("monday");
        break;
    case 3:
        console.log("tuesday");
        break;  
    case 4:
        console.log("Wedensday");
        break;
    case 5:
        console.log("Thursday");
        break; 
    case 6:
        console.log("Friday");
        break;
    case 7:
        console.log("Saturday");
        break;
    default:
        console.log("Invalid day value")                  
    
    }